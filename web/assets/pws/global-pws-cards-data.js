function launchUpfrontRewardPopup(is_existing){ //Existing customer means NO CHANGE OF INCOME

    if(currently_selected_card.applynow_tiger_reward == "ur")
    {
        if(is_existing)
            openWindow(currently_selected_card.applynowexisting_ur, "_form");
        else
            openWindow(currently_selected_card.applynow_ur, "_form");
    }
    else if(currently_selected_card.applynow_tiger_reward == "fr")
    {
        if(is_existing)
            openWindow(currently_selected_card.applynowexisting_fr, "_form");
        else
            openWindow(currently_selected_card.applynow_fr, "_form");
    }
    else
    {
        $('#tiger-card-title').html(currently_selected_card.title);

        $('#tiger-card-apply').unbind('click').click(function (ev) {
            ev.preventDefault();
            var which_reward = $('input[name=tigerCard]:checked', '#tigerCardPopup').val();

            if (which_reward == null || which_reward == '')
            {
                alert('Please select your Rewards plan');
                return;
            }
            else
            {
                // currently_selected_card.applynowexisting_fr
                // currently_selected_card.applynowexisting_ur
                // currently_selected_card.applynow_fr
                // currently_selected_card.applynow_ur

                $(this).trigger('reveal:close');

                if (which_reward == '1')  // UR
                {
                    if(is_existing)
                        openWindow(currently_selected_card.applynowexisting_ur, "_form");
                    else
                        openWindow(currently_selected_card.applynow_ur, "_form");
                } 
                else if (which_reward == '2') // FR
                {
                    if(is_existing)
                        openWindow(currently_selected_card.applynowexisting_fr, "_form");
                    else
                        openWindow(currently_selected_card.applynow_fr, "_form");
                }
            }
        });

        $('#tigerCardPopup').reveal({ animation: 'fade', closeOnBackgroundClick: true });
    }  
}


function launchChangeOfIncomePopup(apply_now_current, apply_now_new) {
    $('#changed-income-title').html(currently_selected_card.title);
	
	if(currently_selected_card.id == "5")
	{
		$('#changed-income-footer-phaseout-title').show();
		$('#changed-income-footer-phaseout-copy').show();
	}
	else
	{
		$('#changed-income-footer-phaseout-title').hide();
		$('#changed-income-footer-phaseout-copy').hide();
	}
	
	
    $('#changed-income-proceed').unbind('click').click(function (ev) {
        ev.preventDefault();
        var has_changed_income = $('input[name=changedIncome]:checked', '#changedIncomePopup').val();

        if (has_changed_income == null || has_changed_income == '') 
        {
            alert('Please select yes or no');
            return;
        } 
        else if (has_changed_income == 'YES') 
        {
            //UR/FR START
            if(currently_selected_card.id == "11")
            {
                launchUpfrontRewardPopup(false);                
            }
            //UR/FR END
            else
            {
                //document.location.href = apply_now_new;
                $(this).trigger('reveal:close');
                openWindow(apply_now_new, "_form");
            }
        } 
        else if (has_changed_income == 'NO') 
        {
            //UR/FR START
            if(currently_selected_card.id == "11")
            {
                //No change of income MEANS is truly existing customer
                launchUpfrontRewardPopup(true);
            }
            //UR/FR END
            else
            {
                //document.location.href = apply_now_current;
                $(this).trigger('reveal:close');
                openWindow(apply_now_current, "_form");
            }
        }
    });

    $('#changedIncomePopup').reveal({ animation: 'fade', closeOnBackgroundClick: true });
}

